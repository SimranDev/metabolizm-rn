const path = require('node:path');
const fs = require('node:fs/promises');
const { withAndroidManifest, withDangerousMod } = require('expo/config-plugins');

/**
 * Keep auth state out of Android Auto Backup.
 *
 * Android backs an app's SharedPreferences up to the user's Google Drive and
 * restores them onto the next device they sign into. expo-secure-store ships
 * rules that include every prefs file and exclude only its own:
 *
 *     <include domain="sharedpref" path="."/>
 *     <exclude domain="sharedpref" path="SecureStore"/>
 *
 * That leaves `com.google.android.gms.signin.xml` in the backup — the legacy
 * Google Sign-In SDK's cache, which holds `defaultGoogleSignInAccount` plus a
 * full account blob (id token, email, display name, photo URL). Two problems:
 *
 * 1. Restoring it onto a new device pre-populates the SDK's cached account, so
 *    `GoogleSignin.signIn()` skips the account chooser on what is genuinely a
 *    first launch — the app silently signs in as whoever used the old device.
 * 2. It is credential material leaving the device in the first place, which is
 *    exactly what the SecureStore exclusion above exists to prevent.
 *
 * These rules re-declare the secure-store exclusions (the manifest attribute
 * takes a single file, so ours has to be a superset, not an addition) and add
 * the Google one. `android/` is regenerated by prebuild, so this is a config
 * plugin rather than an edit to the generated files.
 *
 * Excluded prefs are named without the `.xml` suffix, matching the platform's
 * `sharedpref` domain convention.
 */
const RULES_BASENAME = 'metabolizm_backup_rules';
const EXTRACTION_BASENAME = 'metabolizm_data_extraction_rules';

// Android 11 and lower.
const FULL_BACKUP_CONTENT = `<?xml version="1.0" encoding="utf-8"?>
<!-- Generated by plugins/with-auth-backup-rules.js — do not edit by hand. -->
<full-backup-content>
  <include domain="sharedpref" path="."/>
  <exclude domain="sharedpref" path="SecureStore"/>
  <exclude domain="sharedpref" path="com.google.android.gms.signin"/>
</full-backup-content>
`;

// Android 12 and higher.
const DATA_EXTRACTION_RULES = `<?xml version="1.0" encoding="utf-8"?>
<!-- Generated by plugins/with-auth-backup-rules.js — do not edit by hand. -->
<data-extraction-rules>
  <cloud-backup>
    <include domain="sharedpref" path="."/>
    <exclude domain="sharedpref" path="SecureStore"/>
    <exclude domain="sharedpref" path="com.google.android.gms.signin"/>
  </cloud-backup>
  <device-transfer>
    <include domain="sharedpref" path="."/>
    <exclude domain="sharedpref" path="SecureStore"/>
    <exclude domain="sharedpref" path="com.google.android.gms.signin"/>
  </device-transfer>
</data-extraction-rules>
`;

function withRuleFiles(config) {
  return withDangerousMod(config, [
    'android',
    async (cfg) => {
      const xmlDir = path.join(
        cfg.modRequest.platformProjectRoot,
        'app/src/main/res/xml',
      );
      await fs.mkdir(xmlDir, { recursive: true });
      await fs.writeFile(
        path.join(xmlDir, `${RULES_BASENAME}.xml`),
        FULL_BACKUP_CONTENT,
      );
      await fs.writeFile(
        path.join(xmlDir, `${EXTRACTION_BASENAME}.xml`),
        DATA_EXTRACTION_RULES,
      );
      return cfg;
    },
  ]);
}

function withManifestAttributes(config) {
  return withAndroidManifest(config, (cfg) => {
    const application = cfg.modResults.manifest.application?.[0];
    if (!application) return cfg;
    application.$['android:fullBackupContent'] = `@xml/${RULES_BASENAME}`;
    application.$['android:dataExtractionRules'] = `@xml/${EXTRACTION_BASENAME}`;
    // expo-secure-store's library manifest declares both attributes too; without
    // an explicit replace the merger fails on the conflict rather than
    // preferring the app's value.
    const existing = application.$['tools:replace'];
    const replace = new Set(
      (existing ? existing.split(',') : []).map((s) => s.trim()).filter(Boolean),
    );
    replace.add('android:fullBackupContent');
    replace.add('android:dataExtractionRules');
    application.$['tools:replace'] = [...replace].join(',');
    return cfg;
  });
}

module.exports = function withAuthBackupRules(config) {
  return withManifestAttributes(withRuleFiles(config));
};
